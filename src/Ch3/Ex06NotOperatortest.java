package Ch3;

public class Ex06NotOperatortest {
	public static void main(String[] args) {
		boolean Power = true;
		System.out.printf("Power = %b%n", Power);
		System.out.printf("!Power = %b%n", !Power);
		System.out.printf("!!Power = %b%n", !!Power);
		System.out.printf("!!!Power = %b%n", !!!Power);
	}

}
